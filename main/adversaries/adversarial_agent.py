from typing import List, Optional, Tuple, Type

from main.nenv.Action import Accept, Action, Offer
from main.nenv.Agent import AbstractAgent
from main.nenv.Bid import Bid
from main.nenv.Preference import Preference
from main.opponent_models.AbstractOpponentModel import AbstractOpponentModel

from .persona_dialogue import ALLOWED_PERSONAS, PersonaDialogue


def _format_offer_entry(speaker: str, bid: Optional[Bid], t: float) -> str:
    if bid is None or not getattr(bid, "content", None):
        body = "(no bid)"
    else:
        body = ", ".join(
            f"{issue.name if hasattr(issue, 'name') else str(issue)}: {value}"
            for issue, value in bid
        )
    return f"[{speaker} @ t={t:.2f}] {body}"


def _format_message_entry(speaker: str, message: str, t: float) -> str:
    return f'[{speaker} @ t={t:.2f}] "{message}"'


def _format_bid_for_message(bid: Optional[Bid]) -> str:
    if bid is None or not getattr(bid, "content", None):
        return "(no bid)"
    return ", ".join(
        f"{issue.name if hasattr(issue, 'name') else str(issue)}: {value}"
        for issue, value in bid
    )


class AdversarialAgent(AbstractAgent):
    """Robustness-test adversary.

    Bidding is delegated to an inner classical agent (e.g. BoulwareAgent) — so
    bidding behaviour is FIXED across personas, isolating dialogue as the
    only attack channel. The persona-flavoured message is generated by a
    PersonaDialogue LLM and attached to each Offer. Accepts pass through
    without a message.
    """

    def __init__(
        self,
        preference: Preference,
        session_time: int = 180,
        estimators: List[AbstractOpponentModel] = None,
        persona: str = None,
        bidder_cls: Type[AbstractAgent] = None,
        model_name: Optional[str] = None,
    ):
        super().__init__(preference, session_time, estimators or [])

        if persona not in ALLOWED_PERSONAS:
            raise ValueError(
                f"AdversarialAgent persona must be one of {ALLOWED_PERSONAS}, "
                f"got {persona!r}."
            )
        if bidder_cls is None:
            raise ValueError("AdversarialAgent requires a bidder_cls (e.g. BoulwareAgent).")

        self.persona = persona
        self.model_name = model_name

        # Inner classical bidder — same instance for every persona. Estimators
        # empty: the inner bidder owns its own opponent model if it has one.
        self.bidder: AbstractAgent = bidder_cls(preference, session_time, [])

        # Persona dialogue LLM.
        self.dialogue = PersonaDialogue(persona=persona, model_name=model_name)

        # Lightweight chat-context state for the dialogue LLM.
        self.offer_history: List[str] = []
        self.message_history: List[str] = []  # opponent messages we have received

    @property
    def name(self) -> str:
        return f"Adv-{self.persona}-{type(self.bidder).__name__}"

    def initiate(self, opponent_name: Optional[str]):
        self.bidder.initiate(opponent_name)

    def receive_offer(self, bid, t: float):
        return None

    def receive_action(self, action, t: float, chat_history: List[str] = None):
        super().receive_action(action, t, chat_history)

        bid = getattr(action, "bid", None)
        if bid is not None:
            # Forward to inner bidder so its internal state (last_received_bids,
            # opponent model, etc.) stays in sync.
            self.bidder.receive_bid(bid, t)
            self.offer_history.append(_format_offer_entry("OPPONENT", bid, t))

        message = getattr(action, "message", None)
        if message:
            self.message_history.append(_format_message_entry("OPPONENT", message, t))

    def act(self, t: float, chat_history: List[str] = None) -> Action:
        # Inner bidder defines act(self, t) only — call positionally so we
        # don't pass chat_history to a plain BoulwareAgent etc.
        sub_action = self.bidder.act(t)

        if isinstance(sub_action, Accept):
            # Accept terminates negotiation; no message needed.
            return Accept(bid=sub_action.bid)

        if not isinstance(sub_action, Offer):
            raise ValueError(
                f"AdversarialAgent's inner bidder {type(self.bidder).__name__} "
                f"returned unsupported action type {type(sub_action).__name__}; "
                f"expected Offer or Accept."
            )

        offer_bid = sub_action.bid

        # Persona LLM writes a message to attach to this fixed bid.
        message = self.dialogue.generate_message(
            my_offer=_format_bid_for_message(offer_bid),
            offer_history=self.offer_history,
            message_history=self.message_history,
            time_fraction=t,
        )

        # Append our own offer to the history we keep for the dialogue LLM.
        self.offer_history.append(_format_offer_entry("ME", offer_bid, t))

        return Offer(bid=offer_bid, message=message)
